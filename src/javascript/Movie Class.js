
//Class constructor

export class movie{
    constructor(id, movieName, genre, description, comingSoon,duration, availDate, poster, trailer,) {
        this.id = id;
        this.movieName = movieName;
        this.genre = genre;
        this.description = description;
        this.comingSoon = comingSoon;
        this.duration= duration;
        this.availDate = availDate;
        this.poster = poster;
        this.trailer = trailer;
    }
}